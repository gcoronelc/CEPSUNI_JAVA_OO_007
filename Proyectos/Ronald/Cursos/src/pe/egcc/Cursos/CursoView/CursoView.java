    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        int vacantes = Integer.parseInt(txtVacantes.getText());
        int tiempo = 30;
        int pagoHora;
        double costo;
        String nivel = ComboBoxNivel.getSelectedItem().toString();
        String nombreCurso = ComboBoxCurso.getSelectedItem().toString();
        
        switch (nivel) {
            case "BASICO":
                pagoHora = 60;
                costo = 500;
                break;
            case "INTERMEDIO":
                pagoHora = 80;
                costo = 700;
                break;
            default:
                pagoHora = 100;
                costo = 900;
                break;
        }
               
        CursoDto dto = new CursoDto(vacantes, tiempo, pagoHora, costo);
        Service service = new Service();
        dto = service.ProcesarUtilidad(dto);
        
        txtCurso.setText("" + nombreCurso);
        txtNivel.setText("" + nivel);
        txtVacantesm.setText("" + vacantes);
        txtCosto.setText("" + dto.getCosto());
        txtIngresos.setText("" +dto.getIngresos()); 
        txtPublicidad.setText("" + dto.getGastosPubli());
        txtLogistica.setText("" + dto.getGastosLog());
        txtMateriales.setText(""+ dto.getGastosMat());
        txtPagoProfesor.setText("" + dto.getPagoProf());
        txtUtilidad.setText(""+ dto.getUtilidad());
        
        
    }//GEN-LAST:event_btnProcesarActionPerformed

